#include "../inc/lib.h"

char	*ft_unitoa(wchar_t c)
{
	int		i;
	char	*s;

	i = -1;
	s = ft_strnew(5);
	if (MB_CUR_MAX == 1 && c > 127 && c < 256)
		s[++i] = c;
	else if (c <= 0x7F)
		s[++i] = c;
	else if (c <= 0x7FF)
	{
		s[++i] = (c >> 6) + 0xC0;
		s[++i] = (c & 0x3F) + 0x80;
	}
	else if (c <= 0xFFFF)
	{
		s[++i] = (c >> 12) + 0xE0;
		s[++i] = ((c >> 6) & 0x3F) + 0x80;
		s[++i] = (c & 0x3F) + 0x80;
	}
	else if (c <= 0x10FFFF)
	{
		s[++i] = (c >> 18) + 0xF0;
		s[++i] = ((c >> 12) & 0x3F) + 0x80;
		s[++i] = ((c >> 6) & 0x3F) + 0x80;
		s[++i] = (c & 0x3F) + 0x80;
	}
	return (s);
}
