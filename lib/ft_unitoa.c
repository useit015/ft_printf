#include "../inc/lib.h"

char	*ft_unitoa(wchar_t c, char *s)
{
	if (MB_CUR_MAX == 1 && c > 127 && c < 256)
		*s = c;
	else if (c <= 0x7F)
		*s = c;
	else if (c <= 0x7FF)
	{
		s[0] = (c >> 6) + 0xC0;
		s[1] = (c & 0x3F) + 0x80;
	}
	else if (c <= 0xFFFF)
	{
		s[0] = (c >> 12) + 0xE0;
		s[1] = ((c >> 6) & 0x3F) + 0x80;
		s[2] = (c & 0x3F) + 0x80;
	}
	else if (c <= 0x10FFFF)
	{
		s[0] = (c >> 18) + 0xF0;
		s[1] = ((c >> 12) & 0x3F) + 0x80;
		s[2] = ((c >> 6) & 0x3F) + 0x80;
		s[3] = (c & 0x3F) + 0x80;
	}
	return (s);
}
